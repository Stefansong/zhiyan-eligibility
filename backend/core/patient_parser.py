"""病历识别文本 → 患者结构化字段（带原文出处定位）。"""
from core import llm

_SYSTEM = """你是一名资深临床研究协调员（CRC），负责将患者病历的 OCR 识别文本整理为结构化筛选档案。

要求：
1. 只整理文本中明确存在的信息，绝不推测或编造；OCR 可能有错别字，可按医学常识纠正明显的识别错误（如"血红蛋臼"→"血红蛋白"）
2. 隐私保护：不要输出患者姓名、身份证号、电话、住址；姓名一律用"患者"代替
3. 每个字段必须标注出处：file（病历原文所在文件名，与输入的【文件：xxx】标记一致）、page（页码数字）、raw（原文片段引用）
4. conf 为该字段识别与归纳的置信度（0~1 小数）：原文明确清晰 ≥0.9；需要轻度推断或 OCR 质量差 0.7~0.85；信息模糊 <0.7
5. flag 标记：数值临界 = "borderline"，报告日期久远(>90天)或无日期 = "date"，用药起始时间/疗程不明 = "duration"，轻度异常 = "mild"，由其他字段推算 = "derived"，无需标记 = ""
6. 优先提取与临床试验筛选相关的字段：人口学（年龄/性别/身高体重/BMI）、疾病诊断与病程、关键检验值（带数值单位与日期）、用药史、过敏史、重大病史事件、知情同意状态
7. 病历中完全没有但筛选必需的关键信息（如知情同意书），以 flag="missing"、v="未见记载" 的形式列出
8. fields 控制在 10~20 条，按重要程度排序

严格输出以下 JSON 格式：
{
  "summary": {"sex": "男/女/未知", "age": "年龄数字（识别不到为 0）", "diagnosis": "主要诊断"},
  "fields": [
    {"k": "字段名（如：年龄、HbA1c、二甲双胍）", "v": "值（含单位，如：58 岁、8.2 %）",
     "raw": "病历原文引用", "file": "来源文件名（无来源为空字符串）", "page": 1,
     "conf": 0.95, "flag": ""}
  ]
}"""


def parse(ocr_text: str) -> dict:
    return llm.chat_json(_SYSTEM, f"请将以下病历识别文本整理为结构化筛选档案：\n\n{ocr_text}")
